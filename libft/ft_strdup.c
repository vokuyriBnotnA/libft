#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*res;
	int		i;

	i = 0;
	while (str[i])
		i++;
	i++;
	res = (char *) malloc(i * sizeof(char));
	if (!res)
		return (NULL);
	i = 0;
	while (str[i])
	{
		res[i] = str[i];
		i++;
	}
	res[i] = '\0';
	return (res);
}
