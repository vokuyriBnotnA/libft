#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	unsigned int	n1;
	char			s;

	if (n < 0)
	{
		write(fd, "-", 1);
		n1 = -n;
	}
	else
		n1 = n;
	if (n1 / 10 != 0)
		ft_putnbr_fd(n1 / 10, fd);
	s = n1 % 10 + '0';
	write(fd, &s, 1);
}
